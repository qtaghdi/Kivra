#!/usr/bin/env node
import { copyFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

const repoRoot = process.cwd();
const kivraHome = join(homedir(), ".kivra");
const runtimeDir = join(kivraHome, "trace-runtime");
const shellDir = join(kivraHome, "shell");
const helperPath = join(runtimeDir, "shell-stream.mjs");
const integrationPath = join(shellDir, "zsh-integration.zsh");
const zshrcPath = join(homedir(), ".zshrc");
const sourceLine = `source ${quoteForShell(integrationPath)}`;
const block = [
  "",
  "# >>> kivra shell capture >>>",
  sourceLine,
  "# <<< kivra shell capture <<<",
  ""
].join("\n");

mkdirSync(runtimeDir, { recursive: true });
mkdirSync(shellDir, { recursive: true });
copyFileSync(join(repoRoot, "tools", "kivra-shell-stream.mjs"), helperPath);

writeFileSync(
  integrationPath,
  `# Kivra automatic shell capture for zsh.
# This file is generated by tools/install-shell-capture.mjs.

if [ -n "$KIVRA_SHELL_CAPTURE_LOADED" ]; then
  return
fi

export KIVRA_SHELL_CAPTURE_LOADED=1
export KIVRA_PROJECTS_FILE="${kivraHome}/trace-projects.json"
export KIVRA_STREAM_HELPER="${helperPath}"

autoload -Uz add-zsh-hook

function _kivra_preexec() {
  if [ -n "$KIVRA_CAPTURE_RUN_DIR" ]; then
    return
  fi

  if [ ! -f "$KIVRA_PROJECTS_FILE" ] || [ ! -f "$KIVRA_STREAM_HELPER" ]; then
    return
  fi

  local run_dir
  run_dir="$(node "$KIVRA_STREAM_HELPER" start "$PWD" "$1" "$KIVRA_PROJECTS_FILE" 2>/dev/null)"

  if [ -z "$run_dir" ]; then
    return
  fi

  export KIVRA_CAPTURE_RUN_DIR="$run_dir"
  exec {KIVRA_ORIG_STDOUT}>&1
  exec {KIVRA_ORIG_STDERR}>&2
  exec > >(node "$KIVRA_STREAM_HELPER" stream "$KIVRA_CAPTURE_RUN_DIR" stdout)
  exec 2> >(node "$KIVRA_STREAM_HELPER" stream "$KIVRA_CAPTURE_RUN_DIR" stderr >&2)
}

function _kivra_precmd() {
  if [ -z "$KIVRA_CAPTURE_RUN_DIR" ]; then
    return
  fi

  exec 1>&$KIVRA_ORIG_STDOUT
  exec 2>&$KIVRA_ORIG_STDERR
  exec {KIVRA_ORIG_STDOUT}>&-
  exec {KIVRA_ORIG_STDERR}>&-
  unset KIVRA_CAPTURE_RUN_DIR
  unset KIVRA_ORIG_STDOUT
  unset KIVRA_ORIG_STDERR
}

add-zsh-hook preexec _kivra_preexec
add-zsh-hook precmd _kivra_precmd
`
);

const zshrc = existsSync(zshrcPath) ? readFileSync(zshrcPath, "utf8") : "";

if (!zshrc.includes(sourceLine)) {
  writeFileSync(zshrcPath, `${zshrc.replace(/\s*$/, "")}${block}\n`);
}

console.log(`installed ${integrationPath}`);
console.log(`updated ${zshrcPath}`);

function quoteForShell(value) {
  return `'${value.replaceAll("'", "'\\''")}'`;
}
